    st.success(f"‚úÖ Naƒçteno {len(df)} obchod≈Ø ze strategi√≠: {', '.join(df['strategy'].unique())}")
    
    # Z√°kladn√≠ metriky
    metrics = calculate_portfolio_metrics(df)
    
